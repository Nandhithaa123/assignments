package AssignmentsPsedocode;

public class SwappingNumbers 
{
	public static void main(String[] args) 
	{
		int a=50;
		int b=100;
	    //1.Initialize the temp variable as c
		int c=a;
		c=50;
		//2.Assign the b values in a
		a=b;
		//3.Assign the c values in b
		b=c;
		//4.print a and b
		System.out.println(a);
		System.out.println(b);
	}

}
