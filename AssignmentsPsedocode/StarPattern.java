package AssignmentsPsedocode;

public class StarPattern 
{
	public static void main(String[] args) 
	{
		//1.iterate the vales from 1 till 5
		for(int i=1;i<=5;i++)
		{
			//2.iterate the values 
			for(int j=1;j<=i;j++)
			{
				//3.print *
				System.out.print("*");
			}
			System.out.println();
			
		}
		
	}

}
