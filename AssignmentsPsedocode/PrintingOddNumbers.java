package AssignmentsPsedocode;

public class PrintingOddNumbers 
{
	public static void main(String[] args) 
	{
		//1.iterate the values from 1 till 20
		for(int i=1;i<=20;i++) 
		{
			//2.check the condition 
			if(i%2!=0)
			{
				//3.print i when the condition is true
				System.out.println(i);
			}
			
		}
	}
	

}
